public class Velo {
    private String type;
    private int nbreroues;

    public Velo(String type, int annee) {
        this.type = type;
        this.nbreroues = nbreroues;
    }
    public void setType() {
        this.type = type;
    }
    public void setAnnee(int annee) {
        this.nbreroues = nbreroues;
    }

    public String getType() {
        return type;
    }
    public int getAnnee() {
        return nbreroues;
    }
}
