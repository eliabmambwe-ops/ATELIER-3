public class Moto {
    private String type;
    private String cylindree;

    public Moto(String type, String cylindree) {
        this.type = type;
        this.cylindree = cylindree;
    }
    public void setType() {
        this.type = type;
    }
    public void setcylindree(){
        this.cylindree = cylindree;
    }
    public String getType() {
        return type;
    }
    public String getcylindree() {
        return cylindree;
    }
}
